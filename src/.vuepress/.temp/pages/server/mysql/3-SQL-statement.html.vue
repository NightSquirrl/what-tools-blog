<p>SQL语句中嵌套SELECT语句，称为嵌套查询，又称子查询。</p>
<div class="language-sql line-numbers-mode" data-ext="sql" data-title="sql"><pre v-pre class="language-sql"><code><span class="token keyword">SELECT</span> <span class="token operator">*</span> <span class="token keyword">FROM</span> t1 <span class="token keyword">WHERE</span> column1 <span class="token operator">=</span> <span class="token punctuation">(</span> <span class="token keyword">SELECT</span> column1 <span class="token keyword">FROM</span> t2 <span class="token punctuation">)</span><span class="token punctuation">;</span>
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><p>子查询外部的语句可以是INSERT / UPDATE / DELETE / SELECT 的任何一个。</p>
<h4 id="_7-5-1-分类" tabindex="-1"><a class="header-anchor" href="#_7-5-1-分类"><span>7.5.1  分类</span></a></h4>
<p>根据子查询结果不同，分为：</p>
<p>A. 标量子查询（子查询结果为单个值）</p>
<p>B. 列子查询(子查询结果为一列)</p>
<p>C. 行子查询(子查询结果为一行)</p>
<p>D. 表子查询(子查询结果为多行多列)</p>
<p>根据子查询位置，分为：</p>
<p>A. WHERE之后</p>
<p>B. FROM之后</p>
<p>C. SELECT之后</p>
<h4 id="_7-5-2-标量子查询" tabindex="-1"><a class="header-anchor" href="#_7-5-2-标量子查询"><span>7.5.2 <strong>标量子查询</strong></span></a></h4>
<p>子查询返回的结果是单个值（数字、字符串、日期等），最简单的形式，这种子查询称为标量子查询。</p>
<p>常用的操作符：= &lt;&gt; &gt; &gt;= &lt; &lt;=</p>
<blockquote>
<p>查询 &quot;销售部&quot; 的所有员工信息</p>
<p>完成这个需求时，我们可以将需求分解为两步：</p>
<p>①. 查询 &quot;销售部&quot; 部门ID</p>
<p>②. 根据 &quot;销售部&quot; 部门ID, 查询员工信息</p>
</blockquote>
