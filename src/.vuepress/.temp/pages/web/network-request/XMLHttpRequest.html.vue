</blockquote>
<p>HttpRequest.setRequestHeader(&quot;Content-Type&quot;, &quot;application/json&quot;);</p>
<p>常见的HTTP响应头部</p>
<ul>
<li>
<p>Accept：指定客户端能够接收的内容类型。</p>
</li>
<li>
<p>Accept-Charset：指定客户端能够接收的字符集。</p>
</li>
<li>
<p>Accept-Encoding：指定客户端能够接收的内容编码。</p>
</li>
<li>
<p>Accept-Language：指定客户端能够接收的语言。</p>
</li>
<li>
<p>Authorization：包含客户端提供给服务器以验证自己身份的凭证。</p>
</li>
<li>
<p>Cache-Control：指定请求和响应遵循的缓存机制。</p>
</li>
<li>
<p>Connection：指定与连接相关的选项。</p>
</li>
<li>
<p>Content-Length：指定请求体的长度，以字节为单位。</p>
</li>
<li>
<p>Content-Type：指定请求体的 MIME 类型。</p>
</li>
<li>
<p>Cookie：包含来自客户端的 Cookie 值。</p>
</li>
<li>
<p>Host：指定被请求资源的主机名和端口号。</p>
</li>
<li>
<p>If-Modified-Since：允许在请求中包含条件，只有请求内容在指定日期后或之后修改过才返回它。</p>
</li>
<li>
<p>Referer：包含当前页面的 URL，用于标识请求是从哪个页面发送过来的。</p>
</li>
<li>
<p>User-Agent：包含发起请求的用户代理。</p>
</li>
</ul>
