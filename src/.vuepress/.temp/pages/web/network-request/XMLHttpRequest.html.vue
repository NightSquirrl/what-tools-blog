<p>例如：xhr.send(&quot;foo=bar&amp;lorem=ipsum&quot;);</p>
<p>4.终止请求</p>
<div class="language-javascript line-numbers-mode" data-ext="js" data-title="js"><pre v-pre class="language-javascript"><code>xhr<span class="token punctuation">.</span><span class="token function">abort</span><span class="token punctuation">(</span><span class="token punctuation">)</span><span class="token punctuation">;</span>
