<template><div><h3 id="_1-xmlhttprequest的意义和概念" tabindex="-1"><a class="header-anchor" href="#_1-xmlhttprequest的意义和概念"><span>1.XMLHttpRequest的意义和概念</span></a></h3>
<p>XMLHttpRequest是一个特殊的对象，其目的是作用于服务器交换数据</p>
<h4 id="主要功能" tabindex="-1"><a class="header-anchor" href="#主要功能"><span>主要功能：</span></a></h4>
<pre><code>它可以使网页在不重新加载的情况下更新网页

在页面加载后向服务器发送请求

在页面加载后从服务器接收数据

在后台向服务器发送数据
</code></pre>
<h3 id="_2-属性" tabindex="-1"><a class="header-anchor" href="#_2-属性"><span>2.属性</span></a></h3>
<ul>
<li>
<p>readystate(只读):</p>
<p>返回一个无符号短整型数字，代表请求的状态码</p>
<p>0：表示XMLHttprequest实例已经生成，但是实例的open方法没有被调用</p>
<p>1：表示open方法已经调用，可以使用实例的setRequestHeader()方法设置HTTP请求头信息。此时send方法还没调用</p>
<p>2：表示实例的send方法已经调用，并且服务器返回的头信息和状态码已经收到</p>
<p>3：表示正在接收服务器传来的数据体body部分。这时，如果实例的responseType属性等于text或者空字符串，responseText属性就会包含已经收到的部分信息</p>
<p>4：表示服务器返回的数据已经完全接收，或本次接收已经失败</p>
</li>
<li>
<p>onreadystatechange:</p>
<p>当<strong>readystate</strong>属性发生变化时，调用的事件处理器</p>
<ul>
<li>
<p>responseType:</p>
<p>一个定义响应类型的枚举类</p>
</li>
<li>
<p>response(只读):</p>
<p>返回根据<strong>responseType</strong>的的值来返回对应类型的响应实体</p>
</li>
<li>
<p>status(只读):</p>
<p>返回一个无符号的短整型数字，代表请求的响应状态</p>
</li>
<li>
<p>timeout:</p>
<p>一个无符号长整型数字，代表当前请求的最大请求时间，超出该事件，请求会自动终止</p>
</li>
</ul>
</li>
</ul>
<h3 id="_3-实例方法" tabindex="-1"><a class="header-anchor" href="#_3-实例方法"><span>3.实例方法</span></a></h3>
<ul>
<li>
<p>abort():</p>
<p>如果请求已发出，则立刻终止请求。</p>
</li>
<li>
<p>getAllResponseHeaders():</p>
<p>以字符串形式返回所有使用CRLF分隔的响应头，如果没有收到响应，则返回null。</p>
</li>
<li>
<p>getResponseHeader():</p>
<p>返回包含指定响应头的字符串，如果响应未收到或者响应中不存在改响应头，则返回null。</p>
</li>
<li>
<p>open():</p>
<p>初始化一个请求。</p>
</li>
<li>
<p>send():</p>
<p>发送请求。如果请求是异步的，那么该方法将在请求发送后立即返回。</p>
</li>
<li>
<p>setRequestHeader():</p>
<p>设置HTTP请求表头的值，必须在**open()**之后，**send()**之前调用该方法。</p>
</li>
</ul>
<h3 id="_3-使用xmlhttprequest的方法" tabindex="-1"><a class="header-anchor" href="#_3-使用xmlhttprequest的方法"><span>3.使用XMLHttpRequest的方法</span></a></h3>
<p>1.首先我们需要创建一个XMLHttpRequest对象，才能使用其功能</p>
<div class="language-JavaScript line-numbers-mode" data-ext="JavaScript" data-title="JavaScript"><pre v-pre class="language-JavaScript"><code>const xhr = new XMLHttpRequest();
