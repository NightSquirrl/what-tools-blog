<h3 id="_1-3-java语言的发展" tabindex="-1"><a class="header-anchor" href="#_1-3-java语言的发展"><span>1.3 Java语言的发展</span></a></h3>
<p>三个版本：</p>
<ul>
<li>Java5.0：这是Java的第一个大版本更新。</li>
<li>Java8.0：这个是目前绝大数公司正在使用的版本。因为这个版本最为稳定。</li>
<li>Java15.0：这个是我们课程中学习的版本。</li>
</ul>
<p>解惑：</p>
<p>​	我们学的跟工作中使用的版本不太一样啊。会不会影响以后工作呢？</p>
<p>向下兼容。新的版本只是在原有的基础上添加了一些新的功能而已。</p>
<p>举例：</p>
<p>用8版本开发的代码，用11版本能运行吗？必须可以的。</p>
<p>用11版本开发的代码，用8版本能运行吗？不一定。</p>
<p>如果11版本开发的代码，没有用到9~11的新特性，那么用8是可以运行的。</p>
<p>如果11版本开发的代码，用到了9~11的新特性，那么用8就无法运行了。</p>
